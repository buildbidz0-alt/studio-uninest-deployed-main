const sidebarMenuButtonVariants = cva(
  "flex w-full items-center gap-3 overflow-hidden rounded-lg p-3 text-left text-base outline-none ring-ring transition-all hover:bg-muted focus-visible:ring-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      isActive: {
        true: "primary-gradient text-primary-foreground font-bold shadow-md",
        false: "text-muted-foreground",
      },
    },
    defaultVariants: {
      isActive: false,
    },
  }
)

const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    asChild?: boolean
    isActive?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  } & VariantProps<typeof sidebarMenuButtonVariants>
>(
  (
    {
      asChild = false,
      isActive = false,
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { isMobile, state, setOpenMobile } = useSidebar()
    const displayState = isMobile ? 'expanded' : state;

    const handleClick = (event: React.MouseEvent<HTMLButtonElement>) => {
      if (props.onClick) {
        props.onClick(event);
      }
      if (isMobile) {
        setOpenMobile(false);
      }
    };


    const button = (
      <Comp
        ref={ref}
        data-sidebar="menu-button"
        data-active={isActive}
        className={cn(sidebarMenuButtonVariants({ isActive }), displayState === 'collapsed' && 'justify-center', className)}
        onClick={handleClick}
        {...props}
      >
        {displayState === 'expanded' ? (
            children
        ) : (
            // In collapsed state, only render the icon (the first child)
            Array.isArray(children) ? children[0] : children
        )}
      </Comp>
    )

    if (displayState === 'expanded') {
      return button;
    }
    
    // Determine content for tooltip
    const tooltipContent = tooltip || (Array.isArray(children) && children.length > 1 ? children[1] : null);

    if (!tooltipContent) {
        return button;
    }

    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          hidden={displayState !== "collapsed"}
        >
            {tooltipContent}
        </TooltipContent>
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

